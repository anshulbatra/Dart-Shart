void main() {
  
  //This is a comment
  print("Hello, World!");
  
  //Print strings using double quotes
  print("I am Anshul Batra.");
  
  //Print numbers
  print(3*6); 	//An integer value
  print(4/8); 	//A double value
  
  //Print boolean
  print(true);
  
  //Print using variables
  var a = "Anshul";
  var b = false;
  var c = 5*6/89;
  var d; 			  //An uninitialised variable
  
  print("My name is: $a");
  print("Am I cool: $b");
  print("This is a number: $c");
  print("This is the result of printing an uninitialised variable: $d");
  
}
