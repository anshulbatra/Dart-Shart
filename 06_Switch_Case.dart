void main() {

	// Switch Case Statements: Applicable for only 'int' and 'String'

	String grade = 'A';

	switch (grade) {

		case 'A':
			print("Grade A");
			break;

		case 'B':
			print("Grade B");
			break;

		case 'C':
			print("Grade C");
			break;

		case 'F':
			print("Grade F");
			break;
      
		default:
			print("Invalid Grade");
	}
}
